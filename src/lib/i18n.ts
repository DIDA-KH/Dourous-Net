export type Lang = 'fr' | 'en' | 'ar'
export type Section = keyof typeof T.fr

/** Get current lang from cookie (for Server Components) */
export function getServerLang(): Lang {
  try {
    const cookieStore = cookies()
    const v = cookieStore.get('dn_lang')?.value
    if (v === 'fr' || v === 'en' || v === 'ar') return v
  } catch {}
  return 'fr'
}

/** Server-side translate */
export function ts(lang: Lang, section: string, key: string): string {
  // eslint-disable-next-line @typescript-eslint/no-explicit-any
  const val = (T as any)[lang]?.[section]?.[key] ?? (T as any).fr[section]?.[key] ?? key
  return val as string
}
